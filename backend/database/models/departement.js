const mongoose = require("mongoose");

const departementSchema = new mongoose.Schema({
  nameDepartement: {
    type: String,
    required: true,
  },
  active: {
    type: Boolean,
    default: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("Departement", departementSchema);
